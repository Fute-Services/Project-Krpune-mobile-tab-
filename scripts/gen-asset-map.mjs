// Generates src/offline/assetMap.ts — maps each bundled remote asset (by basename)
// to a static require(). Consumed by src/offline/resolveAsset.ts.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const remoteDir = path.join(root, 'assets', 'remote');
const outFile = path.join(root, 'src', 'offline', 'assetMap.ts');

const files = fs.readdirSync(remoteDir).filter(f => !f.startsWith('.')).sort();

const lines = files.map(f =>
  `  ${JSON.stringify(f)}: require("../../assets/remote/${f}"),`
);

const content = `// AUTO-GENERATED by scripts/gen-asset-map.mjs — do not edit by hand.
// Maps bundled remote-asset basenames to their static require() module id.
export const remoteAssetsByName: Record<string, number> = {
${lines.join('\n')}
};
`;

fs.writeFileSync(outFile, content);
console.log(`Wrote ${outFile} with ${files.length} entries`);
